public class Resolution {
    private int height, Width, BothHW;

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return Width;
    }

    public void setWidth(int width) {
        Width = width;
    }

    public int getBothHW() {
        return BothHW;
    }

    public void setBothHW(int bothHW) {
        BothHW = bothHW;
    }
}
